class DisplayBookmarksController < ApplicationController

	def show
		bookmarks = Bookmark.first(200)
		@y = 1
		@x = 1
		table = bookmarks.map.with_index do |val, index|
			if index % 19 == 0
				@y += 1
				@x = 1
			end
			@x += 1
		  p "#{val.name}", "", " ", "#{@x}", "#{@y}"
		  # '<a href='"#{val.url}"'>"#{val.name}"</a>'
		end
		@table = table.flatten
	end
end

